package com.colourstar.tutorial.javabasic.variable;

public class Example3_Switch 
{
	public static void main(String[] args)
	{
		int iValue = 10;
		
		switch (iValue) 
		{
		case 10:
			System.out.println("here is 10 \n");
			break;
		case 20:
			System.out.println("here is 20 \n");
			break;
		}
	}
}
